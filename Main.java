package com.company;

class three{
    private int one;
    private int two;

    three(){
        System.out.println();
    }

    three(int one){
        System.out.println();
        this.one = one;
    }

    three(int one, int two){
        this.one = one;
        this.two = two;
    }

    void getprint(){
        System.out.println("one = " + one);
        System.out.println("two = " + two);
    }
}

public class Main {

    public static void main(String[] args) {
        three tri = new three();
        tri.getprint();

        tri = new three(2);
        tri.getprint();

        tri = new three(3,4);
        tri.getprint();
    }
}
